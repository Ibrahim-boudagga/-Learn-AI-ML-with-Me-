# Multiple Assignment

print("=== MULTIPLE ASSIGNMENT ===\n")

x, y, z = 1, 2, 3
print(f"x = {x}, y = {y}, z = {z}")

a = b = c = 10
print(f"a = {a}, b = {b}, c = {c}")

# Augmented assignment
count = 5
count += 1
print(f"After count += 1: {count}")

count *= 2
print(f"After count *= 2: {count}")
print()

# Multiple assignment: assign multiple variables at once
# Same value assignment: assign same value to multiple variables
# Augmented assignment: +=, -=, *=, /=, etc.
