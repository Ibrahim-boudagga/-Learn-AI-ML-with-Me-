# Math Module

print("=== MATH MODULE ===\n")

import math

radius = 5
area = math.pi * radius**2

print(f"Radius: {radius}")
print(f"Area of circle: {area:.2f}")
print(f"Square root of 16: {math.sqrt(16)}")
print(f"2 to the power of 8: {math.pow(2, 8)}")
print(f"Ceiling of 3.2: {math.ceil(3.2)}")
print(f"Floor of 3.8: {math.floor(3.8)}")
print(f"Factorial of 5: {math.factorial(5)}")
print()

# Math module provides advanced mathematical functions:
# math.pi : Mathematical constant Ï€
# math.sqrt() : Square root
# math.pow() : Power function
# math.ceil() : Ceiling (round up)
# math.floor() : Floor (round down)
# math.factorial() : Factorial
# math.sin(), math.cos(), math.tan() : Trigonometric functions
