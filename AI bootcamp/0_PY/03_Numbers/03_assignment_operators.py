# Assignment Operators

print("=== ASSIGNMENT OPERATORS ===\n")

x = 10
print(f"Initial x = {x}")

x += 5
print(f"After x += 5: x = {x}")

x -= 3
print(f"After x -= 3: x = {x}")

x *= 2
print(f"After x *= 2: x = {x}")

x /= 4
print(f"After x /= 4: x = {x}")
print()

# Assignment operators combine assignment with operations:
# += : Add and assign
# -= : Subtract and assign
# *= : Multiply and assign
# /= : Divide and assign
# //= : Floor divide and assign
# %= : Modulo and assign
# **= : Exponentiate and assign
