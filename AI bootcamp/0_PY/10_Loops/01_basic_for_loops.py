# Basic For Loops

print("=== BASIC FOR LOOPS ===\n")

# Iterating over a range
print("1. Iterating over a range:")
for i in range(5):
    print(f"Count: {i}")

# Iterating over a list
print("\n2. Iterating over a list:")
fruits = ["apple", "banana", "orange"]
for fruit in fruits:
    print(f"Fruit: {fruit}")

# Iterating over a string
print("\n3. Iterating over a string:")
for char in "Python":
    print(f"Character: {char}")

print("\n=== END OF BASIC FOR LOOPS ===")
