# 2. Comparison Operators
print("2. Comparison Operators:")
x = 10
y = 5

print(f"x = {x}, y = {y}")
print(f"x > y: {x > y}")
print(f"x < y: {x < y}")
print(f"x >= y: {x >= y}")
print(f"x <= y: {x <= y}")
print(f"x == y: {x == y}")
print(f"x != y: {x != y}")

# String comparisons
name1 = "Alice"
name2 = "Bob"
print(f"\nname1 = '{name1}', name2 = '{name2}'")
print(f"name1 < name2: {name1 < name2}")
print(f"name1 == name2: {name1 == name2}")
print(f"name1 != name2: {name1 != name2}")
print()

